#include <stdio.h>
#include <stdbool.h>

int main() {
    bool b1 = true;  // 布尔变量，值为真（true）
    bool b2 = false;  // 布尔变量，值为假（false）
    
    if (b1) {
        printf("b1 is true\n");
    } else {
        printf("b1 is false\n");
    }

    if (b2) {
        printf("b2 is true\n");
    } else {
        printf("b2 is false\n");
    }

    return 0;
}